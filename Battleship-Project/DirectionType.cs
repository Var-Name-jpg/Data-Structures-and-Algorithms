namespace BattleshipFactory {
    /// <summary>
    /// Specifies the orientation of a ship on the game board.
    /// </summary>
    public enum DirectionType {
        /// <summary>
        /// Indicates no direction has been assigned.
        /// </summary>
        None,

        /// <summary>
        /// Indicates the ship is oriented horizontally.
        /// </summary>
        Horizontal,

        /// <summary>
        /// Indicates the ship is oriented vertically.
        /// </summary>
        Vertical
    }
}

